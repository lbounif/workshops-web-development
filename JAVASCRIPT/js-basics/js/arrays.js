
let days = ["Monday", "Tuesday", "Wednesday","Thursday", "Friday"]
console.log(days)

//Add element to the end : push
days.push("Saturday")
console.log(days)

//remove element to the end : pop
days.pop()
console.log(days)

//add element to he begining : unshift
days.unshift("Sunday")
console.log(days)

//remove the first element: shift
days.shift()
console.log(days)

